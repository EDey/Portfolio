        <h4 class="title_stage">Processo inicial:</h4>
        <p>O projeto consiste em produzir um cartaz para o filme Hugo Cabret que transpareça uma ação, no entanto
          para isso precisei antes fazer uma analise do filme, para assim escolher uma ação/movimento que 
          representasse o filme por inteiro.
        </p>
        <p>Depois de assistir o filme duas vezes e fazer um resumo do proprio, cheguei a conclusão que a ação mais
          representativa desse filme era claramente: [Consertar], ja que não necessariamente essa palavra se encaixa
          no pé da letra que acontece no filme, isso quer dizer, não representa só as maquinas, relogios e bugigangas
          sendo consertadas, mas também, uma serie de vidas e relações, até carreira, sendo reparadas ao longo da
          narrativa.
        </p>
        <p>Após a escolha era necessario fazer a conceituação da ação, dessa maneira facilitaria o processo de 
          representação da mesma:</p><br>
        <h4 class="title_stage">Conceituação da Ação:</h4>
        <ol class="lista">
          <li class="group">Participantes:
            <ol>
              <li>Agente: Quem conserta</li>
              <li>Paciente: Quem é Consertado </li>
              <li>Objeto: “Ferramenta” para consertar ( Não necessário )</li>
            </ol>
          </li>
          <li>Trajetória: Extremamente variada, mas na maioria de vai e vem.</li>
          <li>Velocidade: Insignificante</li>
          <li>Frequência: Normalmente repetitiva</li>
          <li>Movimento: Altamente relativo (as vezes simples, as vezes compostos)</li>
          <li>Causa: Algo está quebrado</li>
          <li>Consequência: Algo consertado</li>
        </ol><br>
        <p>Em seguida criei uma nuvem conceitual baseado na coceituação e nos apestcos do filme, essa etapa 
        vai me ajudar muito na hora de indentificar se o cartaz passa a mensagem que eu desejo.</p><br>
        <h4 class="title_stage">Nuvem Conceitual:</h4>
        <p>Velho - Mecânico  - Pobre - Meticuloso</p>
        <p>Engrenagem - Metálico - Relógio</p>
        <p>Maquinário - Escuro - Sujo - Habilidoso</p><br>
        <p>Faltava ainda mais uma coisa pra eu começar a produzir, então iniciei a produção do mood board,
        contendo refrencias, telas do filmes e ppinturas digitais de outros conteudos.</p> <br>
        <h4 class="title_stage">Mood Board:</h4>
        <img src="../../img/projects/hugocabret/process/img1.png" alt="">
        <p>Em seguida eu ja tinha material suficiente para começar a produzir realmente, então começei é claro
        pela representação da ação, pois eu precisava fazer teste com usuarios pra ter o feedback, se realmente
        eles conseguiam ver a ação [Consertar] representada no cartaz.</p><br>
        <h4 class="title_stage">Rascunho:</h4>
        <img src="../../img/projects/hugocabret/process/img2.png" alt="">
        <p>Em seguida como tenho experiencia com pintura digital finalizei o rascunho e ja estava pronto para
        realizar todos os testes necessários</p><br>
        <h4 class="title_stage">Pintura digital:</h4>
        <img src="../../img/projects/hugocabret/process/img3.png" alt="">
        <p>Depois de finalizado fui em buscar de usuario em potencial para realizar os teste de representação
        dos 18 entrevistados, todos reconheceram a ação perfeitamente, 4 pessoas disseram o nome do filme 
        quando perguntado que filme lembrava quando eles viam o cartaz assim. Os teste foram um sucesso!</p>
        <p>Mesmo os teste sendo um sucesso o cartaz estava muito simples e precisava de algo mais inovador,
        sendo assim fui fazer algumas experimentações e teste, abrindo o leque de opções e assim no final
        escolher o melhor e o que mais se adequaria para o cartaz final.</p><br>
